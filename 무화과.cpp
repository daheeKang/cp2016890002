	// int -(2^16) <= a <= (2^16)-1 의 정수 (총 2^32개)
	// ; 은 마침표역할.
	// 파란색은 예약된 단어



	//대입문 : 우변의 "값"을 좌변의 저장장소로 이동(저장)한다.
	//리터럴(literal)소스코드에 적정한 자료형의 값을 직접 표현하는 방법.
	//예) 정수형: 10, 0xF3; 실수형: 3.14, 10f; 0.1E-5; 문자형: 'a', \n; 문자열: "Hello world"


