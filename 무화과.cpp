	//수식(expression) : 연산자와 피연산자들의 조합. 
	// ****모든 수식은 값을 반환(리턴)한다.****
	c=a+b;
	// 연산자(operator):+ ,  피연산자(operand):a, b
	// 연산자 : 연산을 수행하는 기호 = + - / % 등 (7%3 : 7을 3으로 나눈 나머지) && || ! and or not

	//mmlab 강의일정에 연산자부분 참조
	//대입연산자
	a=b=c=100;
	// = : 대입연산자
	// 대입연산자 순서는 오른쪽부터 왼쪽으로 진행. (a=(b=(c=100))); 
	//(c=100)은 2가지 역할 1.우변의 값을 좌변으로 저장하고 2.수식을 리턴 -> 3회 반복(=3개)

